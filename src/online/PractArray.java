package online;

public class PractArray {
    public static void main(String[] args) {
        int [][] array={{100,200},{500,200,600},{700,800,900,550}};
        for (int [] m:array){
            for (int mayur:m){
                System.out.print(mayur+" ");
            }
            System.out.println();
        }
    }
}
